import * as React from "react"
import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "bg-transparent border-none shadow-none", // Removes all visible styling
      className
    )}
    {...props}
  />
))
Card.displayName = "Card"

